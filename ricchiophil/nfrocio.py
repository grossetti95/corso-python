def nfrocio():
    # Il programma chiede all'utente se vuole fare sesso con Nfr. Se l'utente risponde "sì", il programma stampa "Nfr è un ricchione di merda". Se l'utente risponde "no", il programma stampa "Nfr è un ricchione di merda". Se l'utente risponde qualcosa di diverso, il programma stampa "Nfr è un ricchione di merda".
    risposta = input("Vuoi fare sesso con Nfr? (sì/no): ")
    if risposta.lower() == "sì":
        print("Nfr è un ricchione di merda")
    elif risposta.lower() == "no":
        print("Nfr è un ricchione di merda")
    else:
        print("Nfr è un ricchione di merda")

if __name__ == "__main__":
    nfrocio()